""" Basic Math program """
def square(number):
    """
    This function returns the square of a given number
    """
    return number ** 2

def double(number):
    """
    This function returns twice the value of a given number
    """
    return number * 2

def add(a, b):
    """
    This funtion returns the sum of two numbers 
    """
    return a + b
